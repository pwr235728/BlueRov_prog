void Init_GenHtimChanelTables()
{
	htim[0] = &htim2;
	htim[1] = &htim2;
	htim[2] = &htim3;
	htim[3] = &htim3;
	htim[4] = &htim3;

	chanel[0] = TIM_CHANNEL_1;
	chanel[1] = TIM_CHANNEL_2;
	chanel[2] = TIM_CHANNEL_1;
	chanel[3] = TIM_CHANNEL_2;
	chanel[4] = TIM_CHANNEL_3;
}
void Init_StopThrusters()
{
	for(int i=0; i<5; i++)
	{
		__HAL_TIM_SET_COMPARE(htim[i],chanel[i], THRUSTER_MID);
	}
}
void Init_StartPwms()
{
	for(int i=0; i<5; i++)
	{
		HAL_TIM_PWM_Start(htim[i], chanel[i]);
	}
}

void copy_buffer(uint8_t source[], uint8_t destination[], uint32_t length)
{
	while(length--)
	{
		destination[length-1] = source[length-1];
	}
}

uint16_t GetPwmValue(float value)
{
	return THRUSTER_MID + (int16_t)((float)THRUSTER_RANGE*value);
}

void SetThrusters(controll_data_t ctrl_data)
{
	for(int i=0; i<5; i++)
	{
		uint16_t pwm = GetPwmValue(ctrl_data.thrusters[i]);
		__HAL_TIM_SET_COMPARE(htim[i], chanel[i], pwm);
	}
}


